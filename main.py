WORDS_PER_SEND = 30


def tbilisi_today() -> dt.date:
    now_utc = dt.datetime.utcnow()
    now_tbilisi = now_utc + dt.timedelta(hours=TBILISI_OFFSET_HOURS)
    return now_tbilisi.date()


def get_word_batch(today: dt.date):
    """
    Pick 30 words based on which send-day this is.
    Every 2nd calendar day = one send. We use the epoch day // 2 as the batch index.
    This cycles through all words and is deterministic for each day.
    """
    epoch_day = (today - dt.date(2024, 1, 1)).days
    send_index = epoch_day // 2  # changes every 2 days

    total = len(WORDS)
    start = (send_index * WORDS_PER_SEND) % total

    # Pick 30 words with wrap-around
    indices = [(start + i) % total for i in range(WORDS_PER_SEND)]
    return [WORDS[i] for i in indices]


